a = float(input("Digite a medida A: "))
b = float(input("Digite a medida B: "))
c = float(input("Digite a medida C: "))

quadrado = a * a
triangulo = a * b / 2
trapezio = (a + b) * c / 2

print(f"AREA DO QUADRADO = {quadrado:.2f}")
print(f"AREA DO TRIÂNGULO = {triangulo:.2f}")
print(f"AREA DO TRAPÉZIO = {trapezio:.2f}")